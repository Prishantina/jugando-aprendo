let form2 = document.querySelector("#formLetra");

let descripciones = document.createElement("li");
let texto;

function renderJuego(letra, descripcionJuego) {
  const resultado = Math.floor(Math.random() * 3);
  const encontrarLetra = abecedario.find(
    (letrasAbecedario) => letrasAbecedario === letra
  );
  const arrayDescripcionLetra = descripcionJuego.filter(
    (descripcion) => descripcion.letra === letra
  );

  if (encontrarLetra === letra) {
    descripciones.innerHTML = `<p>${arrayDescripcionLetra[resultado].descripcion}</p>`;
    localStorage.setItem(
      "respuestaCorrecta",
      arrayDescripcionLetra[resultado].nombre
    );
    document.querySelector("#mostrarDescImg").appendChild(descripciones);
    texto = arrayDescripcionLetra[resultado].nombre;
  } else {
    descripciones.innerHTML = `<p>Ingresa una letra válida</p>`;
    document.querySelector("#mostrarDescImg").appendChild(descripciones);
  };
};

function mostrarDescrip(e) {
  e.preventDefault();
  let letra = document.getElementById("letraIngresada").value.toLowerCase();
  renderJuego(letra, arrayDescripciones);
};
form2.addEventListener("submit", mostrarDescrip);

